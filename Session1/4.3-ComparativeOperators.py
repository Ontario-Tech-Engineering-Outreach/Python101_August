# Something useful we can do is compare values with eachother. there are 4 main comparative operators.
# Many you might be familiar with

numberOne = 23
numberTwo = 3

# the operators work like this
myBool = (numberOne > numberTwo) # numberOne greater than numberTwo
myBool = (numberOne >= numberTwo) # numberOne greater than or equal to numberTwo
myBool = (numberOne < numberTwo) # numberOne less than numberTwo
myBool = (numberOne <= numberTwo) # numberOne less than or equal to numberTwo